package chapter04;

public class CalcLogic {
	//Q6
	
	public static int tasu(int x, int y) {
		return x + y;
	}
	public static int hiku(int x, int y) {
		return x - y;
	}
	public static int kakeru(int x, int y) {
		return x * y;
	}
	public static int waru(int x, int y) {
		return x / y;
	}
	public static int amari(int x, int y) {
		return x % y;
	}

}
