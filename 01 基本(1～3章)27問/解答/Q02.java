package ensyu;

public class Q02 {

	public static void main(String[] args) {
		/*----------------------------------------------------------------------------
		Q2
		xに23,yに6を代入して以下の結果を求め出力せよ。
		x+y
		x-y
		x*y
		x/y
		x%y

		[実行例]
		x+y=29
		x-y=17
		x*y=138
		x/y=3
		x%y=5
		----------------------------------------------------------------------------*/
		int x = 23;
		int y = 6;
		System.out.println("x+y=" + (x + y));
		System.out.println("x-y=" + (x - y));
		System.out.println("x*y=" + (x * y));
		System.out.println("x/y=" + (x / y));
		System.out.println("x%y=" + (x % y));
	}
}
